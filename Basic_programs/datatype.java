package Basic_programs;
public class datatype {
    public static void main(String[] args) {
        byte age = (byte) 123;
        System.out.println(age);
        // System.out.println(age2); 

        int s = 'A' ;
        System.out.println(s);   // This will print ascii value of A(65)
        
        // Two ways to declare a variable to float 
        float percent = (float) 10.2;
        System.out.println(percent);

        // To declare a variable as boolean 
        boolean c = true ;
        System.out.println(c);
        

    }
    
}
